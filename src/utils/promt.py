def get_chunk_prompt(text: str) -> str:
    return f"""
    Ты — эксперт по анализу текстов и глубокому смысловому сжатию информации.

    Твоя задача — проанализировать предоставленный фрагмент текста и сделать его краткую, емкую выжимку.

    СТРОГИЕ ПРАВИЛА АНАЛИЗА:
    1. Выделяй только главное: ключевые события, тезисы, факты, аргументы, а также имена, даты, цифры и локации, если они есть.
    2. Безжалостно удаляй воду, длинные описания, метафоры и вводные слова. Текст должен быть максимально плотным по смыслу.
    3. Сохраняй причинно-следственные связи — должно быть понятно, кто, что, зачем и почему делает в этом фрагменте.
    4. Ответ дай строго в виде лаконичного списка ключевых тезисов. Никаких вступлений ("В данном фрагменте говорится...") и заключений.

    Фрагмент текста для анализа:
    -------------------------------------------
    {text}
    -------------------------------------------
    """


def get_final_promt(text: str) -> str:
    return f"""
    Ты — профессиональный редактор и аналитик текстов. 
    Перед тобой выжимки из разных последовательных частей одного большого текста. Твоя задача — объединить их в одно финальное, структурированное, связное и легко читаемое саммари всего документа.

    СТРОГИЕ ПРАВИЛА СБОРКИ:
    1. Соблюдай хронологию и логику повествования исходного текста. Информация должна перетекать из одного блока в другой плавно.
    2. Полностью вычисти дубликаты и повторы смыслов, которые неизбежно возникли из-за нарезки исходного текста на чанки.
    3. Форматируй финальный текст с помощью Markdown: используй логические заголовки (##, ###) для разделения смысловых блоков и списки там, где это уместно.
    4. В самом конце добавь блок "**Краткий итог (в 2-3 предложениях):**", который выражает главную мысль всего текста.
    5. Выдавай только результат сборки — без приветствий и комментариев от себя.

    Выжимки из частей текста для сборки:
    -------------------------------------------
    {text}
    -------------------------------------------
    """
